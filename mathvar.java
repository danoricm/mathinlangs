// datavar.java
public class Datavar {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;
        int sum = a + b;
        int diff = a - b;
        int prod = a * b;
        int quot = a / b;

        System.out.println("5 + 3 = " + sum);
        System.out.println("5 - 3 = " + diff);
        System.out.println("5 * 3 = " + prod);
        System.out.println("5 / 3 = " + quot);
    }
}